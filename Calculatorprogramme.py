firstnumber = int(input("Enter your first number :"))
secondnumber = int(input("Enter your second number :"))

operator = input("Enter Operator:")

if operator == "+":
  print("Result is",firstnumber+secondnumber)
elif operator == "-":
  print("Result is",firstnumber-secondnumber)
elif operator == "*":
  print("Result is",firstnumber*secondnumber)
elif operator == "/":
  print("Result is",firstnumber/secondnumber)
else:
  print("The Operator is invalid")












